#include <stdio.h>

int main() {
    int matrix[3][3]; 
    // Input matrix
    printf("Nhap cac phan tu ma tran:\n");
    for (int i = 0; i < 3; i++) {
        for (int j = 0; j < 3; j++) {
            scanf("%d", &matrix[i][j]);
        }
    }

    // Output matrix
    printf("Ma tran vua nhap:\n");
    for (int i = 0; i < 3; i++) {
        for (int j = 0; j < 3; j++) {
            printf("%d ", matrix[i][j]);
        }
        printf("\n");
    }

    return 0;
}